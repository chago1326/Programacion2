public void quitarFila(JTable tabla){
        try {
            DefaultTableModel modelo = (DefaultTableModel) tabla.getModel();
            modelo.removeRow(tabla.getSelectedRow());
            tabla.addRowSelectionInterval(0,0);
            modelo=null;
        } catch (Exception e) {
            
        }
    }

