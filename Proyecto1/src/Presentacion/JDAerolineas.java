    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
        // TODO add your handling code here:
        if(txtIDaerolinea.getText().equals("") || txtNombre.getText().equals("") || ((JTextField) 
                dateAño.getDateEditor().getUiComponent()).getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "No puedes dejar campos en blanco!!");
        
       
        }else{
            String id = txtIDaerolinea.getText();
            String nombre = txtNombre.getText();
            String fun = ConvertirFechaIngreString();
            String tipo = (String) cmbTipo.getSelectedItem().toString();
            String lista = id + "," + nombre + "," + fun + "," + tipo;
            File register = new File("Aerolineas.txt");
            log.InsertarUsuarios(lista, register);
        }
        
        
    }//GEN-LAST:event_btnInsertarActionPerformed

