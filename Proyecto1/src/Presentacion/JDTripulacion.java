    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        // TODO add your handling code here:
        if(txtCedula.getText().equals("")  || txtNombre.getText().equals("") || 
                ((JTextField) dateFechaNa.getDateEditor().getUiComponent()).getText().isEmpty()){
             JOptionPane.showMessageDialog(null, "No puedes dejar campos en blanco!!");
        }else{
        String cedula = txtCedula.getText();
        String nombre = txtNombre.getText();
        String fecha  = ConvertirFechaIngreString();
        String ID     = (String)cmbIDaer.getSelectedItem().toString();
        String rol    = (String)cmbRol.getSelectedItem().toString();
        String Estado = SeleccionRadioButton();
        
        String lista = cedula + "," + nombre + "," + fecha + "," + ID + "," + rol + "," + Estado + ","+ "Tripulante";
        
        File regi = new File("Tripulacion.txt");
        
        log.InsertarUsuarios(lista, regi);
        

        }
        
    }//GEN-LAST:event_btnIngresarActionPerformed
