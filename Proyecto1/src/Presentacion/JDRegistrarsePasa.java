    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
      
        if (txtCedula.getText().equals("") || txtConreaseña.getText().equals("") || 
                txtEdad.getText().equals("")
                || txtNombre.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "No se pueden dejar espacios en blanco!!");
        } else {
            String Cedula = txtCedula.getText();
            String nombre = txtNombre.getText();
            String Edad = txtEdad.getText();
            String contraseña = txtConreaseña.getText();

            String lista = Cedula + "," + contraseña + "," + "Pasajero" + "," + nombre
                    + "," + Edad;
            File archivo = new File("login.txt");
            log.InsertarUsuarios(lista, archivo);
            dispose();
            JFMenuPrincipal er = new JFMenuPrincipal();
            er.pack();
            er.setVisible(true);
        }


    }//GEN-LAST:event_btnRegistrarActionPerformed

    public void soloNumero(JTextField a) {
        a.addKeyListener(new KeyAdapter() {
            public void keyTyped(KeyEvent e) {
                char c = e.getKeyChar();
                if (!Character.isDigit(c)) {
                    getToolkit().beep();
                    e.consume();
                }

            }
        });

    }
     public void soloLetras(JTextField a) {
        a.addKeyListener(new KeyAdapter() {
            public void keyTyped(KeyEvent e) {
                char c = e.getKeyChar();
                if (Character.isDigit(c) == false) {
                } else {
                    e.consume();
                }
            }
        });

    }
